#Strings
print("Me llamo Yenifer")
print("Me llamo \"Yenifer\"") ## Para que Yenifer quede entre comillas sin problema

#Salto de linea con \n
print("Esta es una linea\nY esta es otra linea")  #La n significa ''nueva linea'' quedando una linea arriba y otra abajo, en diferentes lineas
print("Esta es la tercera linea")

#Tabular, agregar un espacio de 4 caracteres
print("\tEsta es la tercera linea")

#Para escribir en ingles y alguna palabra tiene el apostrofe '
print("This isn\'t a number")

#Dobel barra invertida para referirnos a una barra invertida en algún texto
print("Este signo es \\ una barra invertida")



# El carácter "\n" representa un salto de línea, por lo que al incluirlo en una cadena, se 
# imprimirá una nueva línea en ese punto. De esta manera, puedes imprimir múltiples líneas 
# utilizando una única llamada a print().

print("Línea 1\nLínea 2\nLínea 3")

#En este caso, se utilizan las secuencias de escape \t para insertar tabulaciones entre los 
#elementos y lograr el formato de columnas adecuado. Cada letra y tabulación se imprime en 
#una línea separada utilizando llamadas individuales a print().

print("A\tB\tC")
print("D\tE\tF")
print("G\tH\tI")

